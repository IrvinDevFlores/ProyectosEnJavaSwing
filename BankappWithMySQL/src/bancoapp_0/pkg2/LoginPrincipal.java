    private void IngresarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IngresarBtnActionPerformed
        Sesion.userName =  UserTexto.getText();
        Sesion.pass = new String( PassTexto.getPassword());
        
        Archivo archivo = new Archivo("src/Datos/Usuario.txt");
        String filas[] =  archivo.TToString().split("\n");
        
        var usuarios = new ArrayList<Usuario>();
        for(int i = 1; i < filas.length; i++){
             String props[] = filas[i].split(";");
                Usuario usuario = new Usuario();
                usuario.IdUsuario = Integer.parseInt(props[0]);
                usuario.UserName = props[1];
                usuario.Pass = props[2];
                usuario.Estado = props[3];
                usuarios.add(usuario);
        }
        
        for(Usuario u : usuarios){
           if(u.UserName.equals(Sesion.userName) && u.Pass.equals(Sesion.pass)
                   && u.Estado.equals("Habilitado"))
               
           {
               Sesion.IdUser = u.IdUsuario;
               Sesion.sucursal.IdSucursal = SucursalCombo.getSelectedIndex();
               Sesion.sucursal.Nombre = (String) SucursalCombo.getSelectedItem();
                MainForm form = new MainForm();
                form.setVisible(true);
                this.dispose();
               
           } 
        
        } 
    }//GEN-LAST:event_IngresarBtnActionPerformed

